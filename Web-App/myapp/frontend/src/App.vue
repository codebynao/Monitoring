<template>
  <div id="app">
    <router-view />
  </div>
</template>

<script>
export default {
  name: 'App',
  watch: {
    '$route.name': {
      handler() {
        document.body.style.backgroundColor = this.$route.name === 'Login' ? '#222d32' : '#fff'
        document.title = `${this.$route.name} | Monitoring`
      },
      immediate: true
    }
  }
}
</script>

